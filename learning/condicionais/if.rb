#CONDICIONAL SIMPLES

x = 10 

if x == 10
    puts 'x é igual a 10'
end

#CONDICIONAL COMPOSTA (I)

x = 20

if x == 10
    puts 'x é igual a 10'
else
    puts 'x é outro número'
end

#CONDICIONAL COMPOSTA (II)

x = 50

if x == 10
    puts 'x é igual a 10'
elsif x == 20 
    puts 'x é igual a 20'
else
    puts 'x é outro número'
end